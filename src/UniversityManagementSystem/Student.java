    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try{
            stmt = conn.createStatement();
            int StudentId = Integer.parseInt(id.getText());
            String Name = name.getText();
            String FatherName = fatherName.getText();
            String Nic = nic.getText();
            String Email = email.getText();
            String Address = address.getText();
            String Phone = phone.getText();
            String AcademicYear = academicyear.getText();
            String StudentNumber = studentnumber.getText();
            String Faculty = faculty.getText();
            String Department = department.getText();

            String sql = "UPDATE student SET name = '"+Name+"', fathername = '"+FatherName+"', nic = '"+Nic+"', email = '"+Email+"', address = '"+Address+"',"
                    + " phone = '"+Phone+"', academicyear = '"+AcademicYear+"', studentnumber = '"+StudentNumber+"', faculty = '"+Faculty+"', department = '"+Department+"' WHERE id = '"+StudentId+"' ";
            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Data is Successfully Updated");
            setVisible(false);
            ShowStudent object = new ShowStudent();
            object.setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_updateActionPerformed

