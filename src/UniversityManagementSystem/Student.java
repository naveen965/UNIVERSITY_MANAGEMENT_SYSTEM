    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        try{
            stmt = conn.createStatement();

            int StudentId = Integer.parseInt(id.getText());

            String sql = "SELECT * FROM student WHERE id = '"+StudentId+"' ";

            rs = stmt.executeQuery(sql);

            if(rs.next()){
                name.setText(rs.getString("Name"));
                fatherName.setText(rs.getString("FatherName"));
                nic.setText(rs.getString("Nic"));
                email.setText(rs.getString("Email"));
                address.setText(rs.getString("Address"));
                phone.setText(rs.getString("Phone"));
                academicyear.setText(rs.getString("Academicyear"));
                studentnumber.setText(rs.getString("StudentNumber"));
                faculty.setText(rs.getString("Faculty"));
                department.setText(rs.getString("Department"));
            }
            else{
                JOptionPane.showMessageDialog(null, "Record Not Found");
            }
        }
        catch(HeadlessException | NumberFormatException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_searchActionPerformed

